import { displayVideos, displayShort, displayBtns } from './videos.js';

window.addEventListener('DOMContentLoaded', () => {
  let software = items.filter((i) => {
    return i.category === 'software';
  });
  let video = items.filter((i) => {
    return i.video === 'video';
  });
  let item = items.filter((i) => {
    return i;
  });

  let news = items.filter((i) => {
    return i.category === 'news';
  });
  let educational = items.filter((i) => {
    return i.category === 'Educational';
  });
  let shorts = items.filter((i) => {
    return i.category === 'shorts';
  });

  // thumbnails
  // video container
  const VideoContainer = document.querySelector('.video-container');
  displayVideos(VideoContainer, software);

  // education
  const educationalContainer = document.querySelector('.educational');
  displayVideos(educationalContainer, educational);

  // news
  const newsContainer = document.querySelector('.news-container');
  displayVideos(newsContainer, news);

  // shorts
  const shortContainer = document.querySelector('.shorts-container');
  displayShort(shortContainer, shorts);

  // intrest btns
  const BtnsContainer = document.querySelector('.btns-container');
  const btns = [
    'All',
    ...new Set(
      item.map((i) => {
        return i.content;
      })
    ),
  ];
  displayBtns(BtnsContainer, btns);

  // filtered btns
  const homeBtn = [...document.querySelectorAll('.home-btn')];

  homeBtn.map((i) => {
    i.addEventListener('click', (e) => {
      let category = e.currentTarget.textContent;
      let filterdBtns = items.filter((i) => {
        if (i.content === category) {
          return i;
        }
      });
      if (e.target.textContent === 'All') {
        displayVideos(VideoContainer, video);
      } else {
        displayVideos(VideoContainer, filterdBtns);
      }
    });
  });

  const inputform = document.querySelector('.input-form');
  const input = document.querySelector('.input');
  inputform.addEventListener('keyup', () => {
    const value = input.value;
    let searchValue = items.filter((item) => {
      return item.title.toLowerCase().includes(value);
    });
    displayVideos(VideoContainer, searchValue);
  });
});

